                double nilai = Double.parseDouble(txtInput.getText().trim());
                String asal = (String) comboAsal.getSelectedItem();
                String tujuan = (String) comboTujuan.getSelectedItem();
                double hasil = konversiSuhu(nilai, asal, tujuan);

